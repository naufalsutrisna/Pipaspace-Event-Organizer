    private void bSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSaveActionPerformed
        try {
            PreparedStatement statement = con.prepareStatement("INSERT INTO Penyewa (NIKPenyewa, nama, alamat, jenisKelamin, tanggalLahir, noTelp, email) VALUES (?, ?, ?, ?, ?, ?, ?)");
            
            statement.setString(1, inputNIKPenyewa.getText());
            statement.setString(2, inputnama.getText());
            statement.setString(3, inputalamat.getText());
            if(rbLaki.isSelected()){
                statement.setString(4, "Laki-Laki");
            } else {
                statement.setString(4, "Perempuan");
            }
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String date = sdf.format(inputLahir.getDate());
            statement.setString(5, date);         
            statement.setString(6, inputnoTelp.getText());
            statement.setString(7, inputemail.getText());

            statement.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            TampilData();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_bSaveActionPerformed

