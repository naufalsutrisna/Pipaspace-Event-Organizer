    private void bUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bUpdateActionPerformed
        try {
            PreparedStatement stmt = con.prepareStatement(
                "UPDATE Penyewa SET NIKPenyewa=?, nama=?, alamat=?, tanggalLahir=?, jenisKelamin=?, noTelp=?, email=? WHERE NIKPenyewa=?");
            stmt.setString(1, inputNIKPenyewa.getText());
            stmt.setString(2, inputnama.getText());
            stmt.setString(3, inputalamat.getText()); 
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String date = sdf.format(inputLahir.getDate());
            stmt.setString(4, date);
            if(rbLaki.isSelected()){
                stmt.setString(5, "Laki-Laki");
            } else {
                stmt.setString(5, "Perempuan");
            }
            stmt.setString(6, inputnoTelp.getText());
            stmt.setString(7, inputemail.getText());
            stmt.setString(8, inputNIKPenyewa.getText());
            
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            TampilData();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_bUpdateActionPerformed

