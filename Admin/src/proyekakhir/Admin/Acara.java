    private void JBCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBCariActionPerformed
        tbl = new DefaultTableModel();
        tbl.addColumn("ID Acara");
        tbl.addColumn("Jenis Acara");
        tbl.addColumn("Paket");
        tbl.addColumn("Keterangan");
        tbl.addColumn("Estimasi Harga");
        tableAcara.setModel(tbl);

        String sql = "";
        try {
            java.sql.Statement st = con.createStatement();
            if(CBTipeData.getSelectedItem().equals("ID")) {
                String input = InputPencarian.getText();
                if (input.substring(input.length() - 1).equals("%")) {
                    sql = "SELECT * FROM Acara WHERE IDAcara LIKE '" + input.replace("%", "") + "%'";
                } else if (input.substring(0,1).equals("%")) {
                    sql = "SELECT * FROM Acara WHERE IDAcara LIKE '%" + input.replace("%", "") + "'";
                } else if (input.contains(input)) {
                    sql = "SELECT * FROM Acara WHERE IDAcara LIKE '%" + input + "%'";
                }
            } else if (CBTipeData.getSelectedItem().equals("Jenis Acara")) {
                String input = InputPencarian.getText();
                if (input.substring(input.length() - 1).equals("%")) {
                    sql = "SELECT * FROM Acara WHERE jenisAcara LIKE '" + input.replace("%", "") + "%'";
                } else if (input.substring(0,1).equals("%")) {
                    sql = "SELECT * FROM Acara WHERE jenisAcara LIKE '%" + input.replace("%", "") + "'";
                } else if (input.contains(input)) {
                    sql = "SELECT * FROM Acara WHERE jenisAcara LIKE '%" + input + "%'";
                }
            } else if (CBTipeData.getSelectedItem().equals("Paket")) {
                String input = InputPencarian.getText();
                if (input.substring(input.length() - 1).equals("%")) {
                    sql = "SELECT * FROM Acara WHERE paket LIKE '" + input.replace("%", "") + "%'";
                } else if (input.substring(0,1).equals("%")) {
                    sql = "SELECT * FROM Acara WHERE paket LIKE '%" + input.replace("%", "") + "'";
                } else if (input.contains(input)) {
                    sql = "SELECT * FROM Acara WHERE paket LIKE '%" + input + "%'";
                }
            } else if (CBTipeData.getSelectedItem().equals("Keterangan")) {
                String input = InputPencarian.getText();
                if (input.substring(input.length() - 1).equals("%")) {
                    sql = "SELECT * FROM Acara WHERE keterangan LIKE '" + input.replace("%", "") + "%'";
                } else if (input.substring(0,1).equals("%")) {
                    sql = "SELECT * FROM Acara WHERE keterangan LIKE '%" + input.replace("%", "") + "'";
                } else if (input.contains(input)) {
                    sql = "SELECT * FROM Acara WHERE keterangan LIKE '%" + input + "%'";
                }
            } else {
                sql = "SELECT * FROM Acara WHERE estimasiHarga LIKE '%" + InputPencarian.getText() + "%'";
            }

            java.sql.ResultSet res = st.executeQuery(sql);
            while (res.next()) {
                tbl.addRow(new Object[] {
                    res.getString("IDAcara"),
                    res.getString("jenisAcara"),
                    res.getString("paket"),
                    res.getString("keterangan"),
                    res.getString("estimasiHarga")
                });
            }
            inputID.setText("");
            inputJenisAcara.setSelectedIndex(0);
            inputPaket.setSelectedIndex(0);
            inputKeterangan.setText("");
            inputEstimasiHarga.setText("");
            buttonGroup1.clearSelection();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_JBCariActionPerformed

