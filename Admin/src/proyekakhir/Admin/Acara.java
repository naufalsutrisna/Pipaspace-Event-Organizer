    private void bUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bUpdateActionPerformed
        try {
            PreparedStatement stmt = con.prepareStatement(
                "UPDATE Acara SET IDAcara=?, jenisAcara=?, paket=?, keterangan=?, estimasiHarga=? WHERE IDAcara=?");
            stmt.setString(1, inputID.getText());
            
            if(inputJenisAcara.getSelectedItem().equals("Pernikahan")) {
                stmt.setString(2, "Pernikahan");
            } else if(inputJenisAcara.getSelectedItem().equals("Seminar")) {
                stmt.setString(2, "Seminar");
            } else if(inputJenisAcara.getSelectedItem().equals("Ulang Tahun")) {
                stmt.setString(2, "Ulang Tahun");
            } else if(inputJenisAcara.getSelectedItem().equals("Workshop")) {
                stmt.setString(2, "Workshop");
            } else {
                JOptionPane.showMessageDialog(null, "Silahkan pilih bidang kerja yang tersedia!", "Warning", JOptionPane.INFORMATION_MESSAGE);
            }
            
            if(inputPaket.getSelectedItem().equals("Paket 1")) {
                stmt.setString(3, "Paket 1");
            } else if(inputPaket.getSelectedItem().equals("Paket 2")) {
                stmt.setString(3, "Paket 2");
            } else if(inputPaket.getSelectedItem().equals("Paket 3")) {
                stmt.setString(3, "Paket 3");
            } else {
                JOptionPane.showMessageDialog(null, "Silahkan pilih bidang kerja yang tersedia!", "Warning", JOptionPane.INFORMATION_MESSAGE);
            }
            
            stmt.setString(4, inputKeterangan.getText());
            stmt.setString(5, inputEstimasiHarga.getText());
            stmt.setString(6, inputID.getText());

            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            TampilData();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_bUpdateActionPerformed

    private void bDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bDeleteActionPerformed
        try {
            PreparedStatement stmt = con.prepareStatement(
                "DELETE FROM Acara WHERE IDAcara = '" + inputID.getText() + "'"
                + " AND jenisAcara = '" + inputJenisAcara.getSelectedItem() + "'"
                + " AND paket = '" + inputPaket.getSelectedItem() + "'"
                + " AND keterangan = '"+ inputKeterangan.getText()+ "'"
                + " AND estimasiHarga = '"+ inputEstimasiHarga.getText()+ "'");
            
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus", "Pesan", JOptionPane.INFORMATION_MESSAGE);

            inputID.setText("");
            inputJenisAcara.setSelectedItem("");
            inputPaket.setSelectedItem("");
            inputKeterangan.setText("");
            inputEstimasiHarga.setText("");
            buttonGroup1.clearSelection();
            TampilData();
        } catch (Exception e) {
            System.out.println("Error di sini");
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_bDeleteActionPerformed

