    public void TampilData()
    {
        tbl = new DefaultTableModel();
        tbl.addColumn("ID Acara");
        tbl.addColumn("Jenis Acara");
        tbl.addColumn("Paket");
        tbl.addColumn("Keterangan");
        tbl.addColumn("Estimasi Harga");
        tableAcara.setModel(tbl);
        
        try {
            java.sql.Statement statement = con.createStatement();
            String sql = "SELECT * FROM Acara";
            java.sql.ResultSet result = statement.executeQuery(sql);
            while(result.next()) {
                tbl.addRow(new Object[] {
                    result.getString("IDAcara"),
                    result.getString("jenisAcara"),
                    result.getString("paket"),
                    result.getString("keterangan"),
                    result.getString("estimasiHarga")
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }

