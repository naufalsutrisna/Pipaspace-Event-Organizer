    private void bSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSaveActionPerformed
        try {
            PreparedStatement statement = con.prepareStatement("INSERT INTO Acara (IDAcara, jenisAcara, paket, keterangan, estimasiHarga) VALUES (?, ?, ?, ?, ?)");
            
            statement.setString(1, inputID.getText());
            if(inputJenisAcara.getSelectedItem().equals("Pernikahan")) {
                statement.setString(2, "Pernikahan");
            } else if(inputJenisAcara.getSelectedItem().equals("Seminar")) {
                statement.setString(2, "Seminar");
            } else if(inputJenisAcara.getSelectedItem().equals("Ulang Tahun")) {
                statement.setString(2, "Ulang Tahun");
            } else if(inputJenisAcara.getSelectedItem().equals("Workshop")) {
                statement.setString(2, "Workshop");
            }
            if(inputPaket.getSelectedItem().equals("Paket 1")) {
                statement.setString(3, "Paket 1");
            } else if(inputPaket.getSelectedItem().equals("Paket 2")) {
                statement.setString(3, "Paket 2");
            } else if(inputPaket.getSelectedItem().equals("Paket 3")) {
                statement.setString(3, "Paket 3");
            }
            statement.setString(4, inputKeterangan.getText());
            statement.setString(5, inputEstimasiHarga.getText());

            statement.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            TampilData();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_bSaveActionPerformed

